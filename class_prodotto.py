class Prodotto:
    def __init__(self, id, img, titolo, codice, desc_breve, stelle, recensioni, prezzo, prezzo_visibile, qta, descrizione, tab_nutrizione, ingredienti, categoria):
        self.id = id
        self.img = img
        self.titolo = titolo
        self.codice = codice
        self.desc_breve = desc_breve
        self.stelle = stelle
        self.recensioni = recensioni
        self.prezzo = prezzo
        self.prezzo_visibile = prezzo_visibile
        self.qta = qta
        self.descrizione = descrizione
        self.tab_nutrizione = tab_nutrizione
        self.ingredienti = ingredienti
        self.categoria = categoria